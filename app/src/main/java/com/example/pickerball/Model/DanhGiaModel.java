package com.example.pickerball.Model;


public class DanhGiaModel {
    private int ma, maKh, maSan, soSao;
    private String noiDung, ngay;

    public int getMa() { return ma; }
    public void setMa(int ma) { this.ma = ma; }

    public int getMaKh() { return maKh; }
    public void setMaKh(int maKh) { this.maKh = maKh; }

    public int getMaSan() { return maSan; }
    public void setMaSan(int maSan) { this.maSan = maSan; }

    public int getSoSao() { return soSao; }
    public void setSoSao(int soSao) { this.soSao = soSao; }

    public String getNoiDung() { return noiDung; }
    public void setNoiDung(String noiDung) { this.noiDung = noiDung; }

    public String getNgay() { return ngay; }
    public void setNgay(String ngay) { this.ngay = ngay; }
}