package com.example.pickerball.Model;

public class TaiKhoanModel {
    private int maTk;
    private int maKh;
    private int maNv;
    private String tenDangNhap;
    private String matKhau;
    private String vaiTro;
    private String trangThai;
    private String hoTenHienThi;
    private String anhDaiDien;

    public int getMaTk() { return maTk; }
    public void setMaTk(int maTk) { this.maTk = maTk; }

    public String getTenDangNhap() { return tenDangNhap; }
    public void setTenDangNhap(String tenDangNhap) { this.tenDangNhap = tenDangNhap; }

    public String getMatKhau() { return matKhau; }
    public void setMatKhau(String matKhau) { this.matKhau = matKhau; }

    public String getVaiTro() { return vaiTro; }
    public void setVaiTro(String vaiTro) { this.vaiTro = vaiTro; }

    public int getMaKh() { return maKh; }
    public void setMaKh(int maKh) { this.maKh = maKh; }

    public int getMaNv() { return maNv; }
    public void setMaNv(int maNv) { this.maNv = maNv; }

    public String getTrangThai() { return trangThai; }
    public void setTrangThai(String trangThai) { this.trangThai = trangThai; }

    public String getHoTenHienThi() { return hoTenHienThi; }
    public void setHoTenHienThi(String hoTenHienThi) { this.hoTenHienThi = hoTenHienThi; }

    public String getAnhDaiDien() { return anhDaiDien; }
    public void setAnhDaiDien(String anhDaiDien) { this.anhDaiDien = anhDaiDien; }
}
